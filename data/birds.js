const MAIN_REFERENCE_BY_ID = new Map(
  MAIN_COLLECTION_REFERENCE_ROWS.map((row) => [row.id, row]),
);

const WEATHER_BY_REFERENCE = Object.freeze({
  "/": WEATHER_OPTIONS,
  "Arc-en-ciel": ["Arc-en-ciel"],
  "Soleil ou arc-en-ciel": ["Soleil", "Arc-en-ciel"],
  "Pluie ou arc-en-ciel": ["Pluie", "Arc-en-ciel"],
});

const PERIODS_BY_REFERENCE = Object.freeze({
  "/": PERIOD_OPTIONS,
  "12h00 à 6h00": ["Après-midi", "Soir", "Nuit"],
  "6h00 à 00h00": ["Matin", "Après-midi", "Soir"],
  "18h00 à 12h00": ["Soir", "Nuit", "Matin"],
  "12h00 à 0h00": ["Après-midi", "Soir"],
  "00h00 à 12h00": ["Nuit", "Matin"],
});

function referenceWeatherText(value) {
  if (value === "/") return "par tous les temps";
  if (value === "Arc-en-ciel") return "uniquement sous un arc-en-ciel";
  if (value === "Soleil ou arc-en-ciel") return "au soleil ou sous un arc-en-ciel";
  return "sous la pluie ou un arc-en-ciel";
}

function referenceDetails(row) {
  const schedule = row.schedule === "/" ? "à toute heure" : `de ${row.schedule}`;
  return `Emplacement : ${row.location}. Accessible dès le niveau ${row.level}, ${referenceWeatherText(row.weather)} et ${schedule}.`;
}

function applyMainReference(bird, row) {
  return createBird({
    ...bird,
    name: row.name,
    englishName: bird.englishName,
    zones: [row.location],
    weather: WEATHER_BY_REFERENCE[row.weather],
    periods: PERIODS_BY_REFERENCE[row.schedule],
    unlockLevel: row.level,
    details: referenceDetails(row),
    confidence: "vérifié en jeu",
    sources: [],
    verifiedAt: "2026-07-16",
  });
}

export const INITIAL_BIRDS = Object.freeze(
  RAW_BIRDS.flatMap((bird) => {
    const reference = MAIN_REFERENCE_BY_ID.get(bird.id);
    if (reference) return [applyMainReference(bird, reference)];
    if (bird.event || bird.season) return [bird];
    return [];
  }).map((bird) =>
    Object.freeze({
      ...bird,
      // Champ conservé volontairement vide jusqu'à la rédaction manuelle des fiches.
      details: "",
    }),
  ),
);
